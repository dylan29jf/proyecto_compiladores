    private void initValues(PaymentService payment){
        String texto = "Nombre del cliente: " + util.capitalizeFirstLetter(payment.getName() + " " + payment.getLastname()) + "\n" +
                       "Correo Electronico: " + payment.getEmail() + "\n" +
                       "Telefono:" + payment.getCelphone() + "\n" +
                       "Servicio pagado: " + payment.getService() + "\n" +
                       "Clave de rastrego para el pago: " + util.generateRandomString(12);
        
        ImageIcon icon = new ImageIcon(util.GenerateQR(texto, 250));
        txt_qr.setIcon(icon);
        
    }
