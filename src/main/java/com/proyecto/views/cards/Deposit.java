    private void btn_depositActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_depositActionPerformed
        // TODO add your handling code here:
        DecimalFormat df = new DecimalFormat("#.00");
        try {

            double currentBalance = Double.parseDouble(Debit.txt_currentBalance.getText());
            double amount = Double.parseDouble(txt_deposit.getText());

            double updateBalance = currentBalance + amount;
            Debit.txt_currentBalance.setText(String.valueOf(df.format(updateBalance)));
            int response = JOptionPane.showConfirmDialog(null, "Tu deposito fue exitoso", "Deposito exitoso", JOptionPane.DEFAULT_OPTION);

            if (response == 0) {
                this.setVisible(false);
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Por favor introduce un n√∫mero valido.", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btn_depositActionPerformed

