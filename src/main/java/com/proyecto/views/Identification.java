    private void btn_generarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_generarActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat f1 = new SimpleDateFormat("dd/MM/yyyy");
        SimpleDateFormat f2 = new SimpleDateFormat("yyyy/MM/dd");
        String nombre = txt_nombre.getText().toUpperCase();
        String paterno = txt_paterno.getText().toUpperCase();
        String materno = txt_materno.getText().toUpperCase();
        String fecha = f2.format(f1.format(txt_date.getDate().getTime()));
        ButtonModel selectedModel = group_sex.getSelection();
        String sexo= "Hombre".equals(selectedModel.getActionCommand()) ? "H" : "M";
        String entidad = txt_entidad.getSelectedItem().toString().toUpperCase();
        
        ClaveElector elector = new ClaveElector(nombre, fecha, sexo, "MEX", entidad, paterno, materno);
        
        String clave = new Utils().generarClaveElector(elector);
        
        txt_clave.setText(clave);
        
    }//GEN-LAST:event_btn_generarActionPerformed


