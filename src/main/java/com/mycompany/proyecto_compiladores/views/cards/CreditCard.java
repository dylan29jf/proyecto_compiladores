    private void btn_tramitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tramitarActionPerformed
        // TODO add your handling code here:
        try {
            String nombre = txt_name.getText();
            SimpleDateFormat ff = new SimpleDateFormat("dd/MM/yyyy");
            String date = ff.format(txt_date.getDate().getTime());
            Date d = ff.parse(date);
            String direccion = txt_address.getText();
            String correo = txt_email.getText();
            String puesto = txt_booth.getText();
            String salario = txt_salary.getSelectedItem().toString();
            
            customer = new Customer(nombre, d, correo, direccion, puesto, salario);
            
            new CreditCardCustomer(customer).setVisible(true);
            dispose();
            
        } catch (ParseException ex) {
           
        }
    }//GEN-LAST:event_btn_tramitarActionPerformed

